def removeStopWords(texto):
    text = ' '.join([word for word in texto.split() if word not in stopwords])
    return text

def removerPontuacao(texto):
    return re.sub(u'[^a-zA-Z0-9áéíóúÁÉÍÓÚâêîôÂÊÎÔãõÃÕçÇ ]', '', texto)

def removerLinkseMencoes(texto):
    return re.sub(r"(?:\@|https?\://)\S+", "", texto)

def removeRT(texto):
    return re.sub(r"(?<!\w)RT(?!\w)","",texto,flags=re.IGNORECASE)

def limparTexto(texto):
    texto = str(texto).lower()
    texto = removerLinkseMencoes(texto)
    texto = removeRT(texto)
    texto = removeStopWords(texto)
    texto = removerPontuacao(texto)
    return texto